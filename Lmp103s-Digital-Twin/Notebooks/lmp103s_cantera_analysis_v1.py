# ── Master simulation function ─────────────────────────────────
def run_equilibrium(fuel_name, ox_name, OF_mass, P_bar=P_BAR):
    gas    = ct.Solution('gri30.yaml')
    fdata  = FUELS[fuel_name]
    odata  = OXIDIZERS[ox_name]
    P_pa   = P_bar * 1e5

    mole_ratio = OF_mass * (fdata['MW'] / odata['MW'])
    mixture    = {}
    for sp, xf in odata['species'].items():
        mixture[sp] = mixture.get(sp, 0) + xf * mole_ratio
    for sp, m in fdata['formula'].items():
        mixture[sp] = mixture.get(sp, 0) + m

    gas.TPX = 300.0, P_pa, mixture
    gas.equilibrate('HP')

    T     = gas.T
    gam   = gas.cp / gas.cv
    MW    = gas.mean_molecular_weight
    R     = 8314.0 / MW
    Gvdk  = np.sqrt(gam) * (2/(gam+1))**((gam+1)/(2*(gam-1)))
    cstar = np.sqrt(R * T) / Gvdk
    Cf    = Gvdk * np.sqrt(2*gam/(gam-1)*(1-(1e5/P_pa)**((gam-1)/gam)))
    Isp   = Cf * cstar / G0

    top3 = {gas.species_names[i]: round(float(gas.X[i]),4)
            for i in np.argsort(gas.X)[-3:][::-1]}

    return {'T_c':round(T,1), 'Isp':round(Isp,2),
            'c_star':round(cstar,1), 'gamma':round(gam,4),
            'MW_ex':round(MW,3), 'exhaust':top3}

