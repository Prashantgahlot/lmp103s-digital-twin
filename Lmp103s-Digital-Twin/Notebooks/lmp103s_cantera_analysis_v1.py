# Quantifies how uncertainty in LMP-103S composition
# propagates to performance predictions
# ══════════════════════════════════════════════════════════════

np.random.seed(42)
N_SAMPLES = 500   # Monte Carlo samples (500 is fast, 2000 for paper)

# ── Uncertainty sources ───────────────────────────────────────
# Based on Larsson et al. 2011 reported uncertainties
# LMP-103S decomposition products have ±5% composition uncertainty
# Chamber pressure uncertainty ±2 bar (sensor accuracy)
# O/F ratio uncertainty ±0.3 (injector tolerance)

UNCERTAINTIES = {
    'xO2_std' : 0.1657 * 0.05,   # ±5% of nominal
    'xN2_std' : 0.3789 * 0.05,
    'xH2O_std': 0.4554 * 0.05,
    'P_std'   : 1.0,              # ±1 bar (1 sigma)
    'OF_std'  : 0.2,              # ±0.2 O/F ratio
}

print(f"Running Monte Carlo: {N_SAMPLES} samples per condition...")
print("This evaluates uncertainty in LMP-103S composition,")
print("chamber pressure, and O/F ratio simultaneously.\n")

# Focus: Ethanol/LMP103S at OF=6, P=25 bar (nominal optimum)
fname_mc = 'Ethanol'
OF_nom   = 6
P_nom    = 25

T_samples   = []
Isp_samples = []
cstar_samples = []

for i in range(N_SAMPLES):
    # Sample uncertain inputs (Latin Hypercube approximated via normal)
    xO2  = max(0.05, np.random.normal(0.1657, UNCERTAINTIES['xO2_std']))
    xN2  = max(0.05, np.random.normal(0.3789, UNCERTAINTIES['xN2_std']))
    xH2O = max(0.05, np.random.normal(0.4554, UNCERTAINTIES['xH2O_std']))
    # Renormalize
    total = xO2 + xN2 + xH2O
    xO2 /= total; xN2 /= total; xH2O /= total

    P_sample  = max(5, np.random.normal(P_nom, UNCERTAINTIES['P_std']))
    OF_sample = max(1, np.random.normal(OF_nom, UNCERTAINTIES['OF_std']))

    ox_sample = {'O2': xO2, 'N2': xN2, 'H2O': xH2O}
    MW_ox_s   = xO2*32 + xN2*28 + xH2O*18

    try:
        gas = ct.Solution('gri30.yaml')
        fdata = FUELS[fname_mc]
        mole_ratio = OF_sample * (fdata['MW'] / MW_ox_s)
        mixture = {}
        for sp, xf in ox_sample.items():
            mixture[sp] = mixture.get(sp,0) + xf * mole_ratio
        for sp, m in fdata['formula'].items():
            mixture[sp] = mixture.get(sp,0) + m

        gas.TPX = 300.0, P_sample*1e5, mixture
        gas.equilibrate('HP')

        T   = gas.T
        gam = gas.cp / gas.cv
        MW  = gas.mean_molecular_weight
        R   = 8314.0 / MW
        Gvdk= np.sqrt(gam)*(2/(gam+1))**((gam+1)/(2*(gam-1)))
        cs  = np.sqrt(R*T)/Gvdk
        Cf  = Gvdk*np.sqrt(2*gam/(gam-1)*(1-(1e5/(P_sample*1e5))**((gam-1)/gam)))
        Isp = Cf*cs/G0

        T_samples.append(T)
        Isp_samples.append(Isp)
        cstar_samples.append(cs)
    except:
        pass

T_arr   = np.array(T_samples)
Isp_arr = np.array(Isp_samples)
cs_arr  = np.array(cstar_samples)

print(f"Successful samples: {len(T_arr)}/{N_SAMPLES}")
print(f"\nUNCERTAINTY RESULTS — Ethanol/LMP-103S, O/F={OF_nom}, P={P_nom}bar:")
print(f"{'Metric':<20} {'Mean':>9} {'Std':>9} "
      f"{'95% CI Low':>12} {'95% CI High':>12} {'CV%':>7}")
print("-"*72)

for name, arr in [('T_c (K)', T_arr),
                   ('Isp (s)', Isp_arr),
                   ('c* (m/s)', cs_arr)]:
    mean  = np.mean(arr)
    std   = np.std(arr)
    ci_lo = np.percentile(arr, 2.5)
    ci_hi = np.percentile(arr, 97.5)
    cv    = std/mean*100
    print(f"{name:<20} {mean:>9.2f} {std:>9.3f} "
          f"{ci_lo:>12.2f} {ci_hi:>12.2f} {cv:>6.2f}%")

# ── Figure 4: Uncertainty distributions ──────────────────────
fig, axes = plt.subplots(1, 3, figsize=(15, 5))
fig.suptitle(
    f'Monte Carlo Uncertainty Analysis — Ethanol/LMP-103S\n'
    f'O/F={OF_nom}, P={P_nom} bar | N={len(T_arr)} samples\n'
    f'Input uncertainties: composition ±5%, pressure ±1bar, O/F ±0.2',
    fontweight='bold'
)

for ax, (arr, name, unit, color) in zip(axes, [
    (T_arr,   'Chamber Temperature', 'K',     '#2563A8'),
    (Isp_arr, 'Specific Impulse',    's',     '#DC2626'),
    (cs_arr,  'Char. Velocity c*',   'm/s',   '#059669'),
]):
    ax.hist(arr, bins=40, color=color, alpha=0.7, edgecolor='white')
    mean = np.mean(arr); std = np.std(arr)
    ax.axvline(mean, color='black', ls='-',  lw=2, label=f'Mean={mean:.1f}')
    ax.axvline(mean-2*std, color='red', ls='--', lw=1.5,
               label=f'±2σ: [{mean-2*std:.1f}, {mean+2*std:.1f}]')
    ax.axvline(mean+2*std, color='red', ls='--', lw=1.5)
    ax.set_xlabel(f'{name} ({unit})')
    ax.set_ylabel('Frequency')
    ax.set_title(f'{name}\nCV = {std/mean*100:.2f}%')
    ax.legend(fontsize=8)

plt.tight_layout()
plt.savefig('fig4_uncertainty.png', dpi=200, bbox_inches='tight')
plt.show()
print("Figure 4 saved.")

