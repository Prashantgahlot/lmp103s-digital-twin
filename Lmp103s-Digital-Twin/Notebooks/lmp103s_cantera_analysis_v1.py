ax2.axhline(0, color='black', ls='--', lw=1)
ax2.set_xlabel('Mission Δv (m/s)')
ax2.set_ylabel('Extra Propellant Mass (kg)\n[LMP-103S vs Pure O₂]')
ax2.set_title('Propellant Penalty vs Mission Δv\n(all fuels)')
ax2.legend()

