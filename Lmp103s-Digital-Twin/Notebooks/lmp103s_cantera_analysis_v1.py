def biprop_equilibrium(fuel_formula, fuel_MW, OF_mass,
                        oxidizer_type='LMP103S', P_bar=25):
    """
    Bipropellant equilibrium using Cantera.

    Two oxidizer modes:
    - 'LMP103S': ADN decomp products (N2+H2O+O2) — realistic
    - 'pure_O2':  Pure oxygen — theoretical maximum baseline

    fuel_formula: dict of GRI-Mech species and mole amounts
                  representing the fuel elemental composition
    fuel_MW:      molecular weight of fuel (g/mol)
    OF_mass:      oxidizer/fuel mass ratio
    """
    gas = ct.Solution('gri30.yaml')
    P_pa = P_bar * 1e5

    # ── Oxidizer definitions ────────────────────────────────────
    if oxidizer_type == 'LMP103S':
        # ADN-based LMP-103S decomposition products
        # From Larsson et al. 2011 — validated composition
        ox_species = {'O2': 0.1657, 'N2': 0.3789, 'H2O': 0.4554}
        MW_ox = 0.1657*32 + 0.3789*28 + 0.4554*18  # 24.11 g/mol

    elif oxidizer_type == 'pure_O2':
        # Theoretical pure oxygen — maximum performance baseline
        ox_species = {'O2': 1.0}
        MW_ox = 32.0

    elif oxidizer_type == 'H2O2_90':
        # 90% H2O2 for comparison with PoliMi thesis
        # H2O2 → H2O + 0.5 O2 decomposition
        # 90% H2O2 + 10% H2O by mass
        # Decomp: 0.9 mol H2O2 → 0.9 H2O + 0.45 O2
        #         0.1 mol H2O remains
        # Mole fractions after decomp:
        n_H2O = 0.9 + 0.1/18*18  # simplified
        ox_species = {'O2': 0.45/1.45, 'H2O': 1.0/1.45}
        MW_ox = 0.45/1.45*32 + 1.0/1.45*18  # ≈ 22.3

    else:
        raise ValueError(f"Unknown oxidizer: {oxidizer_type}")

    # ── Mass to mole ratio conversion ──────────────────────────
    # OF_mass = mass_ox / mass_fuel
    # moles_ox / moles_fuel = OF_mass * (MW_fuel / MW_ox)
    mole_ratio = OF_mass * (fuel_MW / MW_ox)

    # ── Build mixture ──────────────────────────────────────────
    mixture = {}
    for sp, xf in ox_species.items():
        mixture[sp] = mixture.get(sp, 0) + xf * mole_ratio
    for sp, moles in fuel_formula.items():
        mixture[sp] = mixture.get(sp, 0) + moles * 1.0

    # ── Equilibrate at constant H, P ──────────────────────────
    gas.TPX = 300.0, P_pa, mixture
    gas.equilibrate('HP')

    T_c   = gas.T
    gamma = gas.cp / gas.cv
    MW_ex = gas.mean_molecular_weight
    R_sp  = 8314.0 / MW_ex

    # ── Isp via characteristic velocity method ─────────────────
    Gam    = np.sqrt(gamma) * (2/(gamma+1))**((gamma+1)/(2*(gamma-1)))
    c_star = np.sqrt(R_sp * T_c) / Gam
    Pe     = 1.0e5   # sea level exit pressure
    Cf     = Gam * np.sqrt(2*gamma/(gamma-1)*(1-(Pe/P_pa)**((gamma-1)/gamma)))
    Isp    = Cf * c_star / 9.80665

    # ── Density impulse (mission-level metric) ─────────────────
    # rho_LMP103S ≈ 1240 kg/m3, rho_ethanol ≈ 789 kg/m3
    # rho_mix = rho_ox*rho_f / (OF*rho_f + rho_ox)
    rho_ox = {'LMP103S':1240, 'pure_O2':1140, 'H2O2_90':1390}
    rho_fu = {'C2H4':567, 'CH4':422, 'C2H4_rp1':820}
    # simplified density impulse
    Isp_d  = Isp  # will refine later

    # ── Key exhaust species ────────────────────────────────────
    top5 = np.argsort(gas.X)[-5:][::-1]
    exhaust = {gas.species_names[i]: round(float(gas.X[i]),4)
               for i in top5}

    return {
        'T_c'    : round(T_c, 1),
        'Isp'    : round(Isp, 2),
        'c_star' : round(c_star, 1),
        'gamma'  : round(gamma, 4),
        'MW_ex'  : round(MW_ex, 3),
        'exhaust': exhaust,
    }


